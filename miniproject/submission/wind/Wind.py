_PARAMS_PATH = pathlib.Path(__file__).parent.parent / "params" / "wind_fit_params.csv"


def _load_fit_params(path: pathlib.Path) -> types.SimpleNamespace:
    """Load wind surface-fit parameters from CSV and return them with dot-access.

    Returns a SimpleNamespace so params can be accessed as:
        params.l_qx.A, params.l_qy.phi_rad, params.r_qx.bias, ...
    """
    rows = {}
    with open(path) as f:
        for row in csv.DictReader(f):
            rows[row["component"]] = types.SimpleNamespace(
                A=float(row["A"]),
                phi_rad=float(row["phi_deg"]) * np.pi / 180,
                bias=float(row["bias"]),
            )
    return types.SimpleNamespace(**rows)


